package pl.programming4you.lectures5.lecture1;

public class Task3 {

    public String addition(int firstNumber, int secondNumber) {
        return firstNumber + " plus " + secondNumber + " is " + (firstNumber + secondNumber);
    }

    public String subtraction(int firstNumber, int secondNumber) {
        return firstNumber + " minus " + secondNumber + " is " + (firstNumber - secondNumber);
    }

    public String multiplication(int firstNumber, int secondNumber) {
        return firstNumber + " times " + secondNumber + " is " + (firstNumber * secondNumber);
    }

    public String division(int firstNumber, int secondNumber) {
        return firstNumber + " divided by " + secondNumber + " is " + (firstNumber / secondNumber);
    }
}
